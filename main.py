# Определение функции для запуска уровня
def level_one():
    print("\nУровень 1: Лесная Поляна")
    print("Марио идет спасать принцессу Питч из плена Боузера. Ваш путь начинается с лесной поляны")
    choice = input("Выберите действие: 1. собрать монеты   2. идти дальше  3. сразиться с врагом   4. труба с секретом")

    if choice == "1":
        print("Вы собрали 5 монет! Впереди видна пещера")
        return 5
    elif choice == "2":
        print("Марио ушел дальше, пропустив монеты. Впереди видна пещера")
        return 0
    elif choice == "3":
        print("Марио прыгнул на врага и победил его. Вы собрали 10 монет! Впереди видна пещера")
        return 10
    elif choice == "4":
        print("Марио залез в трубу и нашел секрет с монетами. Вы собрали 50 монет! Выбравшись из трубы была видна пещера")
        return 50
    else:
        print("Некорректный выбор, уровень окончен.")
        return 0


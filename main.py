# Основной метод для игры
def main_game():
    total_coins = 0  # Счетчик монет

    total_coins += level_one()  # Прохождение первого уровня
    if total_coins == 0:  # Проверка на проигрыш
        print("Марио не смог пройти уровень, игра окончена.")
        return

    total_coins += level_two()  # Прохождение второго уровня
    if total_coins == 0:  # Проверка на проигрыш
        print("Марио не смог пройти уровень, игра окончена.")
        return

    total_coins += level_three()  # Прохождение третьего уровня

    print(f"\nВы закончили игру с {total_coins} монетами!")

